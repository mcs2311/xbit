//-------------------------------------------------------------------------------------
package codex.xbit.api.common.datas;
//-------------------------------------------------------------------------------------
import java.io.*;
import java.net.*;
import java.util.*;
import com.fasterxml.jackson.annotation.*;

import codex.common.utils.*;

//-------------------------------------------------------------------------------------
@SuppressWarnings("serial")
public class AbstractData implements Data, Serializable {
    @JsonIgnore
    protected transient Debug debug;
    @JsonIgnore
    protected long id;

    private static final Random random = new Random();
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
    public AbstractData(){
    	id = random.nextLong();
    }

//-------------------------------------------------------------------------------------
    @JsonIgnore
	public void setDebug(Debug _debug){
    	debug = _debug;
	}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
    @JsonIgnore
	public long getId(){
		return id;
	}

//-------------------------------------------------------------------------------------
    @JsonIgnore
	public void setId(long _id){
		id = _id;
	}

//-------------------------------------------------------------------------------------
    @JsonIgnore
	public String toString(){
		return "AbstractData: id="+id;
	}

//-------------------------------------------------------------------------------------
}
//-------------------------------------------------------------------------------------
