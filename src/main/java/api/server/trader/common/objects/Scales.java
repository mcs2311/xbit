package codex.xbit.api.server.trader.common.objects;
//-------------------------------------------------------------------------------------
import java.math.*;
import java.util.*;

import codex.common.utils.*;
import codex.xbit.api.common.configs.*;


//-------------------------------------------------------------------------------------
public class Scales {
    private int amount;
    private int price;

    //---statics:
    public static final Scales DEFAULT = new Scales();

//-------------------------------------------------------------------------------------
    public Scales() {
    	this(12, 12);
    }

//-------------------------------------------------------------------------------------
    public Scales(int _amount, int _price) {
    	amount = _amount;
    	price = _price;
    }

//-------------------------------------------------------------------------------------
    public int getAmount() {
    	return amount;
    }

//-------------------------------------------------------------------------------------
    public void setAmount(int _amount) {
    	amount = _amount;
    }

//-------------------------------------------------------------------------------------
    public int getPrice() {
    	return price;
    }

//-------------------------------------------------------------------------------------
    public void setPrice(int _price) {
    	price = _price;
    }

//-------------------------------------------------------------------------------------
    public String toString() {
    	return "Scales : " + price + " : " + amount;
    }
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
}
